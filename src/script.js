ioHook.on('keydown', event => {
    //console.log(event); // { type: 'mousemove', x: 700, y: 400 }
        if (insideSettings) {
        var getSettingsDisplayVisibility = document.getElementById("settingsModalSw").getAttribute("style");
        if (getSettingsDisplayVisibility == "display: block;") {
            var shortCutReflecter = document.getElementById("ShortcutKeys");
            var keysToText = "";
            if (event.keycode) {
                if (event.altKey) {
                    keysToText += "<kbd>alt</kbd> +"
                }

                if (event.ctrlKey) {
                    keysToText += "<kbd>ctrl</kbd> + "
                }

                if (event.shiftKey) {
                    keysToText += "<kbd>shift</kbd> + "
                }

                if (event.metaKey) {
                    keysToText += "<kbd>meta</kbd> +"
                }
                // let keyCode = event.rawcode;
                // let chrCode = keyCode - 48 * Math.floor(keyCode / 48);
                // let chr = String.fromCharCode((96 <= keyCode) ? chrCode: keyCode);
                keysToText += "<kbd>" + keyboardMap[event.rawcode] + "</kbd> +";
                if (keysToText.endsWith("+")) {
                    keysToText = keysToText.substring(0, keysToText.lastIndexOf("+") - 1);
                }

                shortCutReflecter.innerHTML = keysToText;
                console.log(event);
            }
        }else{
            resetFlags();//user closed modal by clicking on the background div;
        }
    }
});

